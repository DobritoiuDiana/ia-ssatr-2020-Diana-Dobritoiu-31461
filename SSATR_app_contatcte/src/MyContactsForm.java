    public void populateJtable(){       
        ContactQuery cq = new ContactQuery();
        ArrayList<Contact> ctList = cq.contactList(currentUserId);
        String[] colNames = {"Id", "Nume", "Prenume", "Grup", "Telefon", "Email", "Adresa", "Imagine"};
        Object[][] rows = new Object[ctList.size()][8];        
        for (int i = 0; i<= ctList.size()-1; i++){
            rows[i][0] =ctList.get(i).getCid();
            rows[i][1] =ctList.get(i).getNume();
            rows[i][2] =ctList.get(i).getPrenume();
            rows[i][3] =ctList.get(i).getGrup();
            rows[i][4] =ctList.get(i).getTelefon();
            rows[i][5] =ctList.get(i).getEmail();
            rows[i][6] =ctList.get(i).getAdresa();            

            
            ImageIcon pic = new ImageIcon(new ImageIcon
                                         (ctList.get(i).getFoto())
                                         .getImage()
                                         .getScaledInstance(140, 110, Image.SCALE_SMOOTH)); 
          
            rows[i][7] = pic;    
   
      
        }
         
        MyModel mmd = new MyModel(rows, colNames);
        jTable1.setModel(mmd);
        jTable1.setRowHeight(110);
        jTable1.getColumnModel().getColumn(7).setPreferredWidth(140);
    }
    
